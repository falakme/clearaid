"use client";

import { useState } from "react";
import { AnimatePresence, motion } from "framer-motion";
import {
  BookOpenText,
  ChevronDown,
  ExternalLink,
  FileSearch,
  RotateCcw,
  Share2,
} from "lucide-react";
import { Button, buttonVariants } from "@/components/ui/button";
import { Card } from "@/components/ui/card";
import { Markdown } from "@/components/ui/markdown";
import { Item, Stagger } from "@/components/motion";
import { stripEmoji } from "@/lib/text";
import { DataTable } from "./data-table";
import { ProcessDiagram } from "./process-diagram";
import { TaskList } from "./task-list";
import type { TranslateResult } from "@/lib/types";

interface Props {
  result: TranslateResult;
  originalText: string;
  /** Stable key for persisting checklist progress to localStorage. */
  storageKey: string;
  /** Optional "official site" link shown as a primary action. */
  officialUrl?: string;
  onReset: () => void;
}

/** Green/Yellow/Red badge for the AI's self-reported extraction confidence. */
function ConfidenceBadge({ score }: { score: TranslateResult["ai_confidence_score"] }) {
  const style =
    score === "High"
      ? "bg-emerald-100 text-emerald-700"
      : score === "Low"
        ? "bg-red-100 text-red-700"
        : "bg-amber-100 text-amber-800";
  return (
    <span
      className={"inline-flex items-center gap-1 rounded-full px-2.5 py-0.5 text-xs font-bold " + style}
      title="How confident the AI is in the extracted details (including deadlines). Always verify against the original."
    >
      AI confidence: {score}
    </span>
  );
}

function buildShareText(result: TranslateResult): string {
  const tasks = result.task_list.map((t, i) => `${i + 1}. ${stripEmoji(t.task)}`).join("\n");
  const intro = "My ClearAid action plan:";
  const footer = "Generated by ClearAid — paperwork, made plain.";
  return [intro, tasks || "(no tasks)", footer].filter(Boolean).join("\n\n");
}

export function TranslatorResult({
  result,
  originalText,
  storageKey,
  officialUrl,
  onReset,
}: Props) {
  const [showSource, setShowSource] = useState(false);
  const [shareMsg, setShareMsg] = useState("");
  const sourceText = result.source_text || originalText;

  async function sharePlan() {
    const textToShare = buildShareText(result);
    try {
      if (typeof navigator !== "undefined" && navigator.share) {
        await navigator.share({ title: "My ClearAid action plan", text: textToShare });
        return;
      }
      if (typeof navigator !== "undefined" && navigator.clipboard) {
        await navigator.clipboard.writeText(textToShare);
        setShareMsg("Plan copied to clipboard.");
        setTimeout(() => setShareMsg(""), 2500);
      }
    } catch {
      /* user cancelled the share sheet — ignore */
    }
  }

  return (
    <Stagger className="space-y-5">
      {/* 1. Plain-language explanation (Markdown) */}
      <Item>
        <Card>
          <div className="mb-1 flex flex-wrap items-center justify-between gap-2">
            <h2 className="flex items-center gap-2 text-sm font-bold uppercase tracking-wide text-primary">
              <BookOpenText className="h-4 w-4" /> What this means
            </h2>
            <ConfidenceBadge score={result.ai_confidence_score} />
          </div>
          <Markdown>{result.plain_language_explanation_markdown}</Markdown>
        </Card>
      </Item>

      {/* 2. Process visualizer (renders only when steps exist) */}
      <Item>
        <ProcessDiagram steps={result.diagram_steps} />
      </Item>

      {/* 3. Interactive task list + progress (renders only when tasks exist) */}
      <Item>
        <TaskList tasks={result.task_list} storageKey={storageKey} />
        {result.task_list.length > 0 && (
          <div className="mt-3">
            <Button variant="outline" className="w-full" onClick={sharePlan}>
              <Share2 className="h-5 w-5" /> Share plan
            </Button>
            {shareMsg && (
              <p className="mt-2 text-center text-sm text-emerald-700">{shareMsg}</p>
            )}
          </div>
        )}
      </Item>

      {/* 4. Data table (renders only when headers exist) */}
      <Item>
        <DataTable data={result.table_data} />
      </Item>

      {/* Source transparency (Responsible AI) */}
      <Item>
        <Card>
          <button
            onClick={() => setShowSource((s) => !s)}
            className="flex min-h-tap w-full items-center justify-between rounded-md text-left text-base font-semibold text-muted-foreground hover:text-foreground"
            aria-expanded={showSource}
          >
            <span className="flex items-center gap-2">
              <FileSearch className="h-5 w-5" /> Show original source
            </span>
            <ChevronDown
              className={"h-5 w-5 transition-transform " + (showSource ? "rotate-180" : "")}
            />
          </button>
          <AnimatePresence initial={false}>
            {showSource && (
              <motion.div
                key="src"
                initial={{ height: 0, opacity: 0 }}
                animate={{ height: "auto", opacity: 1 }}
                exit={{ height: 0, opacity: 0 }}
                transition={{ duration: 0.3, ease: [0.22, 1, 0.36, 1] }}
                className="overflow-hidden"
              >
                <p className="mb-2 mt-3 text-sm text-muted-foreground">
                  The exact text ClearAid read to generate the above. Verify any deadline
                  or figure against this source.
                </p>
                <pre className="max-h-80 overflow-auto whitespace-pre-wrap break-words rounded-md bg-muted/60 p-4 text-sm text-muted-foreground">
                  {sourceText}
                </pre>
              </motion.div>
            )}
          </AnimatePresence>
        </Card>
      </Item>

      {/* Direct action + reset */}
      {officialUrl && (
        <Item>
          <a
            href={officialUrl}
            target="_blank"
            rel="noopener noreferrer"
            className={buttonVariants({ size: "lg", className: "w-full" })}
          >
            <ExternalLink className="h-5 w-5" /> Go to official site
          </a>
        </Item>
      )}

      <Item>
        <Button variant="ghost" className="w-full" onClick={onReset}>
          <RotateCcw className="h-5 w-5" /> Translate another document
        </Button>
      </Item>

      <p className="pb-4 text-center text-sm text-muted-foreground">
        ClearAid only explains and organizes. It never submits anything — you stay in
        control of signing and submitting.
      </p>
    </Stagger>
  );
}
